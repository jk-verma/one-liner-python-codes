'''
Problem    : CALCULATE THE EXPONENTIAL OF A NUMBER
Description: The exponential function calculates the result of raising a given base to a specified
             exponent using the exponentiation operator (**).
@Author    : Dr. J. K. Verma
@Date      : 2025-09-20
'''

def exponential(base, exponent):
    return base ** exponent
result = exponential(2, 3)
print(result)
# Output: 8